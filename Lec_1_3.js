//data-types
// 1-Primitive - 7
// 2-Non-Primitive - Object, Array


//Primitive DataType-1 Number
var num1 = 30;
console.log(num1);
console.log(typeof(num1));

//DataType-2 String
var name1 = "Mihir";
console.log(name1);
console.log(typeof(name1));

//DataType-3 Boolean
var bl = true;
console.log(bl);
console.log(typeof(bl));

//DataType-4 NULL  (object)
var empt = null;
console.log(empt);
console.log(typeof(empt));

//DataType-5 Undefined
var obj;
console.log(obj);
console.log(typeof(obj));

//DataType-6 BigInt
var BigNum = BigInt(221584887845151515);
console.log(BigNum);
console.log(typeof(BigNum));

//DataType-7 Symbol  Never used
var sym = Symbol("#@");
console.log(sym);
console.log(typeof(sym));