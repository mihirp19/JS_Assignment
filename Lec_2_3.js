//Increment
x = 5;
console.log(x++); //post-increment
console.log(x);
console.log(++x); //pre-increment
console.log(--x); //pre-decrement
console.log(x--);
console.log(x); //post-decrement

y = 20;
console.log(y) //20
console.log(y++) //20
console.log(y++) //21 22
console.log(++y) //22 23
console.log(y) //23
console.log(++y) //24

console.log(--y);
console.log(y--);
console.log(--y);
console.log(--y);
console.log(y--);
console.log(--y);